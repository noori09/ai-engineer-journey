# Count from 0 to 4
print("Count from 0 to 4")
for i in range(5):
    print(i)

# Count from 1 to 10
print("\nCount from 1 to 10")
for i in range(1,11):
    print(i)

# Even numbers from 0 to 20
print("\n Even numbers from 0 to 20")
for i in range(0,21,2):
    print(i)

# Countdown from 10 to 1
print("\n Countdown from 10 to 1")
for i in range(10,0,-1):
    print(i)

# Multiplication table for 7
print("\n Multiplication table for 7")
for i in range(7,71,7):
    print(i)

# other way for multiplication
print("\n Multiplication table for 13")
num = 13
for i in range(1,11):
    print(f"{num}x{i} = {num * i}")